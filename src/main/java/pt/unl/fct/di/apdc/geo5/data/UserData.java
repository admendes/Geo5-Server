package pt.unl.fct.di.apdc.geo5.data;

public class UserData {
	
	public String username;
	public String name;
	public String email;	

	
	public UserData() {
		
	}
	
	public UserData(String username, String name, String email) {
		
			this.username = username;
			this.name = name;
			this.email = email;
	}
	
}
